        <h3 style="margin-top:50px;">Yoga Postures that improve concentration</h3>
        <ol>
            <li>Tadasana (Mountain Pose)</li>
            <p>
                Tadasana is the base from which all the yoga postures emerge and is referred as the ‘mother of asanas’. Regular practice of Tadasana increases the levels of oxygen in your spinal cord and the brain and strengthens your nervous system. Tadasana improves concentration and boosts energy.
            </p>
            <img src="https://newra.in/dss/images/yoga-ta.jpg" width="100%" height="auto">
            <li>Vrikshasna (Tree Pose)</li>
            <p>
                Vrikshasana is one of the simplest of yoga asanas. Vrikshasana (Tree Pose) helps improve balance, gives your body a proper posture and boosts concentration. If practiced regularly, over time Vrikshasana builds tremendous inner and outer strength.
            </p>
            <img src="https://newra.in/dss/images/yoga-va.png" width="100%" height="auto">
            <li>Savasana (Corpse Pose)</li>
            <p>
                Savasana is a relaxed yoga posture. In Savasana (Corpse Pose) you are relaxed but in a state of awareness. You are aware of your breath, the different parts of your body and your innter strength. To boost concentration, all you need is to practice savasana regularly for 10 minutes, it helps you reboot and feel alive.
            </p>
            <img src="https://newra.in/dss/images/yoga-sa.jpg" width="100%" height="auto">
            <li>Padmasana (Lotus Pose)</li>
            <p>
                Padmasana is a cross-legged yoga posture which helps you improve your concentration levels by calming the mind. In the beigning, try counting from 200 to 1 slowly, without skipping any number. This Yoga posture will help you channelize your thoughts in a single direction, aiding concentration.
            </p>
            <img src="https://newra.in/dss/images/yoga-pa.jpg" width="100%" height="auto">
        </ol>
        <h3 style="margin-top:50px;">Must Do Exercises</h3>
        <ol>
            <li>Suryanamaskar (Sun Salutation)</li>
            <p>
                Suryanamaskar is one of the yoga poses that every student must practice for a healthy mind and body. Suryanamaskar includes 12 poses, engaging different parts and organs of your body. Regular practice of Suryanamaskar makes your body energetic and active. Suryanamaskar develops strength, stability and flexibility of your body and mind. You will benefit Suryanamaskar yoga asana if you do at least 3 rounds every morning, before you start studying.
            </p>
            <img src="https://newra.in/dss/images/yoga-sn.jpeg" width="100%" height="auto">
            <li>Vajrasana (Hand Under Foot yoga pose)</li>
            <p>
                Vajrasana is one of the powerful asanas for students with memory problems. In this yoga asana , you are supposed to kneel down on a flat surface and ensure that your ankles, toes and knees touch the ground. You should close your eyes and relax for a couple of minutes or even half hour in Vajrasana Vajrasana helps in calming your mind and if practiced regularly this yoga asana helps to improve your concentration levels..
            </p>
            <img src="https://newra.in/dss/images/yoga-vra.jpg" width="100%" height="auto">
            <li>Padahasthasana (Hand under Foot yoga pose)</li>
            <p>
                Padahasthasana an intense standing forward bend yoga posture is one of the basic yoga asanas. The benefits of Yoga are included in Padahasthasana as it strengthens your spine and legs and improves blood circulation to your brain, thus making your more alert and focussed.
            </p>
            <img src="https://newra.in/dss/images/yoga-pda.jpg" width="100%" height="auto">
        </ol>
        <h3 style="margin-top:50px;">Mediation Yoga to improve your studies</h3>
        <ol>
            <li>Bhramari Pranayama</li>
            <p>
                Bhramari Pranayama is a recommended yoga for students. In Bhramari Pranayama the breathing sounds like humming of a bee. Regular practice of Bhramari Pranayama relieves you from tension and refreshes your mind and keeps you alert.
            </p>
            <img src="https://newra.in/dss/images/yoga-bp.jpg" width="100%" height="auto">
            <li>Anulom-Vilom Pranayama</li>
            <p>
                Anulom-Vilom Pranayama is a meditation yoga involving deep breathing in and out without making any sound. Anulom-Vilom Pranayama improves your memory power, and improves your eye sight. Additionally, Anulom-Vilom Pranayama blocks the flow of carbon dioxide into your arteries helping in the blood purification process, strengthing your memory and vitality of the key organs.
            </p>
            <img src="https://newra.in/dss/images/yoga-anulom.jpg" width="100%" height="auto">
            <li>Kapalbhathi Pranayama</li>
            <p>
                Kapalbhathi Pranayama is a powerful meditation yoga focusing on forceful exhalation. Kapalbhathi Pranayama reduces belly fat, which students accumulate while studying for long hours, and thereby helps in quick weight loss. This mediation yoga balances your body’s sugar levels and improves blood circulation
            </p>
